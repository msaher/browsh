package main

import (
	"os"
	"text/template"
)

var tokens = []string {
	"TokenWord",
	"TokenFd",
	"TokenString",
	"TokenDupOut",
	"TokenOut",
	"TokenIn",
	"TokenPipe",
	"TokenOrIf",
	"TokenAndIf",
	"TokenEOF",
	"TokenAppend",
	"TokenError",
	"TokenBlock",
}

var tmpl = ` // Generated by gen/token_gen.go. DO NOT EDIT
package shell

type TokenType int

const (
	{{.First}} TokenType = iota
	{{ range .Rest -}}
	{{.}}
	{{end}}
)

func (ttype TokenType) String() string {
	switch ttype {
	case {{.First}}:
		return "{{.First}}"
	{{ range .Rest -}}
	case {{.}}:
		return "{{.}}"
	{{ end }}
	default:
		panic("unreachable")
	}
}
`

func main() {
	data := struct {
		First string
		Rest  []string
	}{First: tokens[0], Rest: tokens[1:]}

	file, err := os.Create("./token_types.go")
	if err != nil {
		panic(err)
	}
	defer file.Close()

	t := template.Must(template.New("whatever").Parse(tmpl))
	t.Execute(file, data)
}
